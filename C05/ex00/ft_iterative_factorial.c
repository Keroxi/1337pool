int	ft_iterative_factorial(int nb)
{
	int	i;
	int	j;

	i = nb - 1;
	j = nb;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (j > 1)
	{
		nb = i * nb;
		i--;
		j--;
	}
	return (nb);
}
