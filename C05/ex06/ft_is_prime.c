int	ft_is_prime(int nb)
{
	int	n;

	n = 2;
	if (nb <= 1)
		return (0);
	if (nb <= 3)
		return (1);
	while ((n * n) <= nb)
	{
		if (nb % n == 0)
			return (0);
		n++;
	}
	return (1);
}
