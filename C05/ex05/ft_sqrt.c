int	ft_sqrt(int nb)
{
	int	n;

	n = 2;
	if (nb < 0 || nb == 2147483647)
		return (0);
	if (nb == 1)
		return (1);
	while (n < nb)
	{
		if (nb == n * n)
			return (n);
		else
			n++;
	}
	return (0);
}
